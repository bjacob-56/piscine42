int	ft_plus(int a, int b)
{
	return (a + b);
}

int	ft_moins(int a, int b)
{
	return (a - b);
}

int	ft_fois(int a, int b)
{
	return (a * b);
}

int	ft_div(int a, int b)
{
	return (a / b);
}

int	ft_mod(int a, int b)
{
	return (a % b);
}
